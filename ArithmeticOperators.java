public class ArithmeticOperators {
  public static void main(String[] args) {
    int a = 10;
    int b = 2;
    int c;

    c = a + b;
    c = a - b;
    c = a * b;
    c = a / b;
    c = a % b;

    c += b;
    c -= b;
    c *= b;
    c /= b;
    c %= b;

    c++;
    c--;
    ++c;
    --c;

    System.out.print(c);

  }
}
